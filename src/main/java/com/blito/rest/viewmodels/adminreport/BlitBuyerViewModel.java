package com.blito.rest.viewmodels.adminreport;


public class BlitBuyerViewModel {
	long userId;
	String firstname;
	String lastname;
	long blitId;
	String trackCode;
	String mobile;
	String blitTypeName;
	public long getUserId() {
		return userId;
	}
	public void setUserId(long userId) {
		this.userId = userId;
	}
	public String getFirstname() {
		return firstname;
	}
	public void setFirstname(String firstName) {
		this.firstname = firstName;
	}
	public String getLastname() {
		return lastname;
	}
	public void setLastname(String lastName) {
		this.lastname = lastName;
	}
	public long getBlitId() {
		return blitId;
	}
	public void setBlitId(long blitId) {
		this.blitId = blitId;
	}
	public String getTrackCode() {
		return trackCode;
	}
	public void setTrackCode(String trackCode) {
		this.trackCode = trackCode;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getBlitTypeName() {
		return blitTypeName;
	}
	public void setBlitTypeName(String blitTypeName) {
		this.blitTypeName = blitTypeName;
	}
	
	
	
}
